export type LegalDoc = 'terms' | 'privacy' | 'refund' | 'content';

const TABS: { id: LegalDoc; label: string }[] = [
  { id: 'terms', label: 'Пользовательское соглашение' },
  { id: 'privacy', label: 'Конфиденциальность' },
  { id: 'refund', label: 'Возврат средств' },
  { id: 'content', label: 'Правила контента' },
];

export function Legal({ initial = 'terms' }: { initial?: LegalDoc }) {
  const [doc, setDoc] = useState<LegalDoc>(initial);

  return (
    <div className="legal">
      <div className="section-head"><h1>Документы</h1></div>
      <div className="legal-tabs">
        {TABS.map((t) => (
          <button key={t.id} className={`chip ${doc === t.id ? 'active' : ''}`} onClick={() => setDoc(t.id)}>
            {t.label}
          </button>
        ))}
      </div>
      <div className="card panel legal-body">
        {doc === 'terms' && <Terms />}
        {doc === 'privacy' && <Privacy />}
        {doc === 'refund' && <Refund />}
        {doc === 'content' && <Content />}
        <p className="legal-meta">
          Дата вступления в силу: {EFFECTIVE_DATE}. По любым вопросам:{' '}
          <a href={SUPPORT_TELEGRAM_URL} target="_blank" rel="noopener noreferrer">
            Telegram @{SUPPORT_TELEGRAM}
          </a>{' '}
          или {CONTACT_EMAIL}.
        </p>
      </div>
    </div>
  );
}

